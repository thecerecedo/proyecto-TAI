public class equipo_propietarios {
    int equipo_propID;
    int velas;
    int anclas;
    int cuerdas;
    int lanchas;
    int salvavidas;
    int refrigeradores;
    int brujulas;
    int indicadores;
    int estufas;
    int radios;

    public equipo_propietarios() {
    }

    public equipo_propietarios(int equipo_propID, int velas, int anclas, int cuerdas, int lanchas, int salvavidas, int refrigeradores, int brujulas, int indicadores, int estufas, int radios) {
        this.equipo_propID = equipo_propID;
        this.velas=velas;
        this.anclas=anclas;
        this.cuerdas=cuerdas;
        this.lanchas=lanchas;
        this.salvavidas=salvavidas;
        this.refrigeradores=refrigeradores;
        this.brujulas=brujulas;
        this.indicadores=indicadores;
        this.estufas=estufas;
        this.radios=radios;
    }



    }
