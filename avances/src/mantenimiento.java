public class mantenimiento {
    int mantenimientoID;
    int limpieza;
    int cambios_de_aceite;
    int reparaciones;
    public mantenimiento(){}
    public mantenimiento(int mantenimientoID, int limpieza, int cambios_de_aceite, int reparaciones){
        this.mantenimientoID = mantenimientoID;
        this.limpieza = limpieza;
        this.cambios_de_aceite = cambios_de_aceite;
        this.reparaciones = reparaciones;
    }
}
